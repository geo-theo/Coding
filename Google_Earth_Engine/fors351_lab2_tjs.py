dataset = ee.ImageCollection('LANDSAT/LC08/C02/T1_L2').filterDate(
    '2021-07-01', '2021-08-01'
) #this code chooses our image and the date range

# Applies scaling factors.
def apply_scale_factors(image):
  optical_bands = image.select('SR_B.').multiply(0.0000275).add(-0.2)
  thermal_bands = image.select('ST_B.*').multiply(0.00341802).add(149.0)
  return image.addBands(optical_bands, None, True).addBands(
      thermal_bands, None, True
  )

#  Satellite data is stored as scaled integers, not real physical values.
#     Without this step, pixel values would not be physically meaningful.
# Optical bands (SR_B*) are converted to unitless surface reflectance
#     Thermal bands (ST_B*) are converted to temperature in Kelvin
# We used a function because the dataset contains many images. If we applied
#     this to every image the code would get very redundant.

dataset_scaled = dataset.map(apply_scale_factors)

#you will now visualize the data in RGB
visualization = {
    'bands': ['SR_B4', 'SR_B3', 'SR_B2'],
    'min': 0.0,
    'max': 0.3,
}

m = geemap.Map()
m.set_center(-113.9940, 46.8721, 8)
m.add_layer(dataset_scaled, visualization, 'True Color (432)')
m

